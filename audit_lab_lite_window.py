def audit_symbol(
    symbol: str,
    out_dir: Path,
    baseline_dir: str = None,
    parquet_dir: str = "data_cache",
    model_dir: str = "regime_models",
) -> Dict:
    print(f"\n{'='*70}")
    print(f"AUDIT {symbol}")
    print(f"{'='*70}")
    result = {"symbol": symbol, "modes": {}}

    # Mode A — extract from existing CSV
    print(f"  Mode A (CURRENT, ~5000 bars hardcoded)...")
    try:
        sig_a = extract_mode_a_signatures(symbol, baseline_dir=baseline_dir)
        result["modes"]["A_current_5000"] = {
            "signatures_global": sorted(sig_a),
            "n_signatures": len(sig_a),
            "source": "existing presets CSV",
        }
        print(f"    → {len(sig_a)} unique signatures")
    except FileNotFoundError as e:
        print(f"    SKIP: {e}")
        result["modes"]["A_current_5000"] = {"error": str(e)}
        return result

    # Mode B1 — full-history regime-agnostic
    print(f"  Mode B1 (FULL-HISTORY agnostic, USE_CLUSTERING=False)...")
    try:
        b1 = run_simulation_full_history(
            symbol, use_clustering=False,
            parquet_dir=parquet_dir, model_dir=model_dir,
        )
        result["modes"]["B1_full_agnostic"] = {
            "signatures_global": sorted(b1["signatures_global"]),
            "n_signatures": len(b1["signatures_global"]),
            "n_bars_total": b1["n_bars_total"],
            "time_range": b1["time_range"],
            "elapsed_s": round(b1["elapsed_s"], 2),
        }
        print(f"    → {len(b1['signatures_global'])} signatures over {b1['n_bars_total']} bars in {b1['elapsed_s']:.1f}s")
    except Exception as e:
        print(f"    ERROR: {e}")
        result["modes"]["B1_full_agnostic"] = {"error": str(e)}
        b1 = None

    # Mode B2 — full-history per-cluster
    print(f"  Mode B2 (FULL-HISTORY per-cluster, USE_CLUSTERING=True)...")
    try:
        b2 = run_simulation_full_history(
            symbol, use_clustering=True,
            parquet_dir=parquet_dir, model_dir=model_dir,
        )
        per_cluster_serializable = {
            str(k): sorted(v) for k, v in b2["signatures_per_cluster"].items()
        }
        result["modes"]["B2_full_per_cluster"] = {
            "signatures_global": sorted(b2["signatures_global"]),
            "signatures_per_cluster": per_cluster_serializable,
            "signatures_union": sorted(b2["signatures_union"]),
            "n_signatures_union": len(b2["signatures_union"]),
            "n_bars_total": b2["n_bars_total"],
            "n_clusters": b2["n_clusters"],
            "time_range": b2["time_range"],
            "elapsed_s": round(b2["elapsed_s"], 2),
        }
        print(f"    → union {len(b2['signatures_union'])} signatures over {b2['n_bars_total']} bars in {b2['elapsed_s']:.1f}s")
        for k, sigs in b2["signatures_per_cluster"].items():
            print(f"      cluster {k}: {len(sigs)} sigs")
    except Exception as e:
        print(f"    ERROR: {e}")
        result["modes"]["B2_full_per_cluster"] = {"error": str(e)}
        b2 = None

    # Overlap report
    pools = {"A": sig_a}
    if b1:
        pools["B1"] = b1["signatures_global"]
    if b2:
        pools["B2_union"] = b2["signatures_union"]
        pools["B2_global"] = b2["signatures_global"]
    result["overlap"] = overlap_report(pools)
    print("\n  Overlap (Jaccard):")
    for k, v in result["overlap"]["jaccard"].items():
        print(f"    {k}: {v}")

    # Persist per-symbol JSON
    out_path = out_dir / f"audit_{symbol.replace('/', '_').replace('USDT','')}.json"
    with open(out_path, "w", encoding="utf-8") as f:
        json.dump(result, f, indent=2)
    print(f"  → {out_path}")
    return result


def write_csvs_for_symbol(symbol: str, out_dir: Path,
                            parquet_dir: str = "data_cache",
                            model_dir: str = "regime_models") -> Dict[str, Path]:
    """Generate B1 (agnostic) and B2 (per-cluster) presets CSVs for symbol.

    Writes:
        out_dir/B1/presets_<SYM>USDT.csv
        out_dir/B2/presets_<SYM>USDT.csv

    Returns dict {'B1': path, 'B2': path}.
    """
    sym_clean = symbol.replace("/", "").replace("USDT", "")
    paths = {}

    print(f"  generating B1 CSV ({symbol})...")
    b1 = run_simulation_full_history(
        symbol, use_clustering=False,
        parquet_dir=parquet_dir, model_dir=model_dir,
        return_full_results=True,
    )
    p_b1 = out_dir / "B1" / f"presets_{sym_clean}USDT.csv"
    write_presets_csv(p_b1, b1["global_results"], cluster_results=None)
    print(f"    → {p_b1} ({len(b1['global_results'])} rows, {b1['elapsed_s']:.1f}s)")
    paths["B1"] = p_b1

    print(f"  generating B2 CSV ({symbol})...")
    b2 = run_simulation_full_history(
        symbol, use_clustering=True,
        parquet_dir=parquet_dir, model_dir=model_dir,
        return_full_results=True,
    )
    p_b2 = out_dir / "B2" / f"presets_{sym_clean}USDT.csv"
    n_cluster_rows = sum(len(v) for v in b2["cluster_results"].values())
    write_presets_csv(p_b2, b2["global_results"], cluster_results=b2["cluster_results"])
    print(f"    → {p_b2} ({len(b2['global_results'])} global + {n_cluster_rows} cluster rows, {b2['elapsed_s']:.1f}s)")
    paths["B2"] = p_b2
    return paths


def main():
    p = argparse.ArgumentParser(description="Audit lab_lite TOTAL_CANDLES window scope")
    p.add_argument("--symbols", nargs="+",
                    default=["BTC/USDT", "ONDO/USDT", "SOL/USDT"],
                    help="symbols to audit (default: BTC + ONDO + SOL)")
    p.add_argument("--baseline-dir", default=None,
                    help="dir with backed-up presets CSV (defaults to output/production)")
    p.add_argument("--parquet-dir", default="data_cache",
                    help="dir with full-history parquet files")
    p.add_argument("--model-dir", default="regime_models",
                    help="dir with GMM regime models")
    p.add_argument("--out", default=None,
                    help="output dir (default: audit_lab_lite_results_<TS>)")
    p.add_argument("--write-csvs", action="store_true",
                    help="Write B1/B2 presets CSV in productive format (consumable by "
                          "regime_walk_forward.py --presets-dir <out>/B1|B2)")
    args = p.parse_args()

    ts = datetime.now(timezone.utc).strftime("%Y%m%d_%H%M%S")
    out_dir = Path(args.out or f"audit_lab_lite_results_{ts}")
    out_dir.mkdir(parents=True, exist_ok=True)
    print(f"OUTPUT DIR: {out_dir}")

    summary = {"ts": ts, "symbols": [], "per_symbol": {}}

    if args.write_csvs:
        print(f"\n[mode] write_csvs → emitting B1/B2 productive CSVs for {args.symbols}")
        for sym in args.symbols:
            paths = write_csvs_for_symbol(
                sym, out_dir,
                parquet_dir=args.parquet_dir,
                model_dir=args.model_dir,
            )
            summary["per_symbol"][sym] = {"csv_paths": {k: str(v) for k, v in paths.items()}}
            summary["symbols"].append(sym)
        with open(out_dir / "summary_csvs.json", "w", encoding="utf-8") as f:
            json.dump(summary, f, indent=2, default=str)
        print(f"\n✓ CSV generation complete — {out_dir}/")
        return 0

    for sym in args.symbols:
        r = audit_symbol(
            sym, out_dir,
            baseline_dir=args.baseline_dir,
            parquet_dir=args.parquet_dir,
            model_dir=args.model_dir,
        )
        summary["symbols"].append(sym)
        summary["per_symbol"][sym] = r

    # Aggregate summary table
    print(f"\n{'='*70}")
    print("SUMMARY TABLE — Jaccard overlap CURRENT(A) vs B1 vs B2")
    print(f"{'='*70}")
    print(f"{'SYMBOL':<12} | {'|A|':>4} | {'|B1|':>4} | {'|B2u|':>5} | {'A∩B1':>6} | {'A∩B2u':>6} | {'B1∩B2u':>7}")
    print("-" * 70)
    for sym, r in summary["per_symbol"].items():
        ov = r.get("overlap", {}).get("jaccard", {})
        sizes = r.get("overlap", {}).get("sizes", {})
        a = sizes.get("A", 0)
        b1n = sizes.get("B1", 0)
