# ---------------------------------------------------------------------------
# Parquet loader (replaces fetch_all_candles for audit modes B1/B2)
# ---------------------------------------------------------------------------

def load_full_parquet(symbol: str, parquet_dir: str = "data_cache") -> pd.DataFrame:
    """Load full-history parquet for symbol. Returns df with [timestamp, OHLCV].

    Normalizes schema: data_cache parquet uses 'timestamp_ms' int64; lab_lite
    expects 'timestamp' as datetime64. Convert ms → datetime.
    """
    sym_clean = symbol.replace("/", "").replace("USDT", "")
    p = Path(parquet_dir) / f"{sym_clean}USDT_1h.parquet"
    if not p.exists():
        raise FileNotFoundError(f"parquet not found: {p}")
    df = pd.read_parquet(p)
    if "timestamp" not in df.columns and "timestamp_ms" in df.columns:
        df = df.rename(columns={"timestamp_ms": "timestamp"})
        df["timestamp"] = pd.to_datetime(df["timestamp"], unit="ms")
    elif "timestamp" in df.columns and not pd.api.types.is_datetime64_any_dtype(df["timestamp"]):
        df["timestamp"] = pd.to_datetime(df["timestamp"], unit="ms")
    cols = ["timestamp", "open", "high", "low", "close", "volume"]
    return df[cols].reset_index(drop=True)


# ---------------------------------------------------------------------------
# Signature extraction
# ---------------------------------------------------------------------------

def preset_signature(fast_entry, slow_entry) -> str:
    """Build canonical signature 'FAST(period)/SLOW(period)' for overlap comparison.

    Trend is excluded — derived post-hoc as slow×4 (see result_to_preset_tuple).
    p1/p2 ignored (cluster of similar variants under same family/period).
    """
    f_type, f_period, _, _ = fast_entry
    s_type, s_period, _, _ = slow_entry
    f_name = ll.MA_NAMES.get(f_type, f"T{f_type}")
    s_name = ll.MA_NAMES.get(s_type, f"T{s_type}")
    return f"{f_name}({f_period})/{s_name}({s_period})"


# ---------------------------------------------------------------------------
# Mode A — CURRENT (read backed-up presets from baseline preservation)
# ---------------------------------------------------------------------------

def extract_mode_a_signatures(symbol: str, baseline_dir: str = None) -> Set[str]:
    """Mode A: extract preset signatures from existing presets CSV
    (already generated by productive lab_lite with TOTAL_CANDLES=5000).

    Uses output/production/presets_<SYM>USDT.csv if available, else
    falls back to baseline_dir/presets_<SYM>USDT.csv.
    """
    sym_clean = symbol.replace("/", "").replace("USDT", "")
    candidate_paths = []
    if baseline_dir:
        candidate_paths.append(Path(baseline_dir) / f"presets_{sym_clean}USDT.csv")
    candidate_paths.append(Path("output/production") / f"presets_{sym_clean}USDT.csv")
    csv_path = None
    for cp in candidate_paths:
        if cp.exists():
            csv_path = cp
            break
    if csv_path is None:
        raise FileNotFoundError(
            f"presets_{sym_clean}USDT.csv not found in {candidate_paths}"
        )
    df = pd.read_csv(csv_path)
    sigs = set()
    for _, row in df.iterrows():
        sig = f"{row['fast_type']}({int(row['fast_period'])})/{row['slow_type']}({int(row['slow_period'])})"
        sigs.add(sig)
    return sigs


# ---------------------------------------------------------------------------
# Mode B1/B2 — FULL-HISTORY simulation
# ---------------------------------------------------------------------------

def run_simulation_full_history(
    symbol: str,
    use_clustering: bool,
    parquet_dir: str = "data_cache",
    model_dir: str = "regime_models",
    min_trades_cluster: int = 30,
    min_pf_cluster: float = 1.2,
    top_n_cluster: int = 6,
    min_trades_global: int = 10,
    top_n_global: int = 14,
    return_full_results: bool = False,
) -> Dict:
    """Run lab_lite v5e simulation over FULL parquet history.

    Args:
        use_clustering: True for Mode B2 (per-cluster), False for Mode B1 (agnostic).

    Returns dict with:
        signatures_global: Set[str] — global preset pool signatures
        signatures_per_cluster: Dict[int, Set[str]] — per-cluster pools (B2 only)
        signatures_union: Set[str] — union of global + per-cluster pools
        n_bars: int
        time_range: Tuple[str, str]
        elapsed_s: float
    """
    t0 = time.time()
    df = load_full_parquet(symbol, parquet_dir=parquet_dir)
    n_bars_total = len(df)
    ts_start = str(df["timestamp"].iloc[0])
    ts_end = str(df["timestamp"].iloc[-1])

    # Pre-calculate all MAs (continuous series)
    close, fast_arr, slow_arr, trend_arr, fast_cat, slow_cat, trend_cat = ll.precalculate_all_mas(df)
    n_fast, n_slow, n_trend = len(fast_cat), len(slow_cat), len(trend_cat)
    n_configs = n_fast * n_slow * n_trend

    # Warmup
    max_slow_period = max(p for _, p, _, _ in slow_cat)
    max_trend_period = max((p for _, p, _, _ in trend_cat if p > 0), default=0)
    warmup = max(max_slow_period, max_trend_period) * 3

    # Cluster labels
    if use_clustering:
        labels, cluster_info = ll.load_and_apply_regime_model(
            symbol, df, model_dir=model_dir
        )
        n_clusters = cluster_info["n_clusters"]
        cluster_labels_np = labels.astype(np.int64)
    else:
        n_clusters = 1
        cluster_labels_np = np.zeros(n_bars_total, dtype=np.int64)

    test_start = warmup

    # EMA reference
    ema_f = ll.calc_ema(close, ll.EMA_FAST)
    ema_s = ll.calc_ema(close, ll.EMA_SLOW)
    ema_t = ll.calc_ema(close, ll.EMA_TREND)
    ema_pnl, ema_trades, ema_raw_crosses, _, _ = ll.simulate_ema_pnl(
        close, ema_f, ema_s, ema_t, ll.COMMISSION_ROUND_TRIP,
        test_start, warmup, cluster_labels_np, n_clusters,
    )

    # Run candidates simulation
    cand_pnl, cand_trades, cand_raw_crosses, cl_pnl, cl_trades, cl_gp, cl_gl = \
        ll.run_simulation_v5e(
            close, fast_arr, slow_arr, trend_arr,
            n_fast, n_slow, n_trend,
            ll.COMMISSION_ROUND_TRIP, test_start, warmup,
            cluster_labels_np, n_clusters,
        )

    # Global scoring (analogous to process_symbol lines 771-792)
    valid = cand_trades >= min_trades_global
    excess_cross = np.where(
        ema_raw_crosses > 0,
        np.maximum(0.0, (cand_raw_crosses.astype(np.float64) - ema_raw_crosses) / ema_raw_crosses),
        0.0,
    )
    excess_cross = np.minimum(excess_cross, 1.0)
    cross_penalty = 1.0 - 0.3 * excess_cross
    if ema_pnl > 0:
        pnl_ratio = cand_pnl / ema_pnl
    else:
        pnl_ratio = cand_pnl / 10.0
    score = pnl_ratio * cross_penalty
    score_valid = np.where(valid, score, -999.0)
    sorted_idx = np.argsort(-score_valid)

    # Build global top-N: collect ranked candidate dicts for both signature
    # extraction AND CSV writing (productive-format compatible).
    global_signatures: Set[str] = set()
    global_results: List[Dict] = []  # res dicts compatible with productive CSV writer
    for idx in sorted_idx:
        if score_valid[idx] <= -999.0:
            break
        fi = idx // (n_slow * n_trend)
        remainder = idx % (n_slow * n_trend)
        si = remainder // n_trend
        ti = remainder % n_trend
        sig = preset_signature(fast_cat[fi], slow_cat[si])
        if sig in global_signatures:
            continue
        global_signatures.add(sig)
        global_results.append({
            "fast": fast_cat[fi], "slow": slow_cat[si], "trend": trend_cat[ti],
            "score": float(score[idx]),
            "pnl": float(cand_pnl[idx]),
            "trades": int(cand_trades[idx]),
            "raw_crosses": int(cand_raw_crosses[idx]),
            "excess_cross": float(excess_cross[idx]),
        })
        if len(global_results) >= top_n_global:
            break

    # Per-cluster selection (B2 only — B1 has n_clusters=1, just one cluster)
    signatures_per_cluster: Dict[int, Set[str]] = {}
    cluster_presets_dicts: Dict[int, List[Dict]] = {}
    if use_clustering:
        all_configs_arr = cand_pnl  # use cand_pnl as placeholder
        cluster_presets_dicts = ll.select_top_per_cluster(
            all_configs_arr, cl_pnl, cl_trades, cl_gp, cl_gl,
            n_clusters, fast_cat, slow_cat, trend_cat,
            n_slow, n_trend,
            min_trades=min_trades_cluster, min_pf=min_pf_cluster,
            top_n=top_n_cluster,
        )
        for k in range(n_clusters):
            sigs_k = set()
            for res in cluster_presets_dicts.get(k, []):
                sigs_k.add(preset_signature(res["fast"], res["slow"]))
            signatures_per_cluster[k] = sigs_k

    union = set(global_signatures)
    for s in signatures_per_cluster.values():
        union |= s

    elapsed = time.time() - t0
    result = {
        "signatures_global": global_signatures,
        "signatures_per_cluster": signatures_per_cluster,
        "signatures_union": union,
        "n_bars_total": n_bars_total,
        "time_range": (ts_start, ts_end),
        "n_clusters": n_clusters,
        "n_configs": n_configs,
        "warmup": warmup,
        "elapsed_s": elapsed,
        "use_clustering": use_clustering,
    }
    if return_full_results:
        result["global_results"] = global_results
        result["cluster_results"] = cluster_presets_dicts
    return result


# ---------------------------------------------------------------------------
# CSV writer (productive format compatible with regime_walk_forward load_presets)
# ---------------------------------------------------------------------------

def write_presets_csv(out_csv: Path, global_results: List[Dict],
                       cluster_results: Dict[int, List[Dict]] = None) -> None:
    """Write presets CSV in productive lab_lite v5e format.

    Columns:
        rank,fast_type,fast_period,fast_p1,fast_p2,
        slow_type,slow_period,slow_p1,slow_p2,
        trend_type,trend_period,trend_p1,trend_p2,
        score,pnl,trades,raw_crosses,excess_cross,family,
        cluster_id,cluster_pf,cluster_pnl,cluster_trades

    Trend derived as slow×4 same type (matches result_to_preset_tuple).
    Global rows have cluster_id=-1 and blank cluster_pf/pnl/trades.
    Per-cluster rows have rank='C{k}_{r}' and cluster_id=k.
    """
    out_csv.parent.mkdir(parents=True, exist_ok=True)
    with open(out_csv, "w", encoding="utf-8") as f:
        f.write(
            "rank,fast_type,fast_period,fast_p1,fast_p2,"
            "slow_type,slow_period,slow_p1,slow_p2,"
            "trend_type,trend_period,trend_p1,trend_p2,"
            "score,pnl,trades,raw_crosses,excess_cross,family,"
            "cluster_id,cluster_pf,cluster_pnl,cluster_trades\n"
        )
        for r, res in enumerate(global_results, 1):
            pt = ll.result_to_preset_tuple(res)
            fam = f"{ll.ma_family(res['fast'])}/{ll.ma_family(res['slow'])}"
            f.write(
                f"{r},{pt[0]},{pt[1]},{pt[2]},{pt[3]},"
                f"{pt[4]},{pt[5]},{pt[6]},{pt[7]},"
                f"{pt[8]},{pt[9]},{pt[10]},{pt[11]},"
                f"{res['score']:.6f},{res['pnl']:.4f},{res['trades']},"
                f"{res['raw_crosses']},{res['excess_cross']:.4f},{fam},"
                f"-1,,,\n"
            )
        if cluster_results:
            for k in sorted(cluster_results.keys()):
                for r, res in enumerate(cluster_results[k], 1):
                    pt = ll.result_to_preset_tuple(res)
                    fam = f"{ll.ma_family(res['fast'])}/{ll.ma_family(res['slow'])}"
                    f.write(
                        f"C{k}_{r},{pt[0]},{pt[1]},{pt[2]},{pt[3]},"
                        f"{pt[4]},{pt[5]},{pt[6]},{pt[7]},"
                        f"{pt[8]},{pt[9]},{pt[10]},{pt[11]},"
                        f"{res.get('score', 0):.6f},{res.get('global_pnl', 0):.4f},"
                        f"{res.get('cluster_trades', 0)},"
                        f"0,0.0000,{fam},"
                        f"{k},{res.get('cluster_pf', 0):.4f},"
                        f"{res.get('cluster_pnl', 0):.4f},"
                        f"{res.get('cluster_trades', 0)}\n"
                    )


# ---------------------------------------------------------------------------
# Overlap metrics
# ---------------------------------------------------------------------------

def jaccard(a: Set[str], b: Set[str]) -> float:
    if not a and not b:
        return 1.0
    inter = a & b
    union = a | b
    return len(inter) / len(union) if union else 0.0


def overlap_report(pools: Dict[str, Set[str]]) -> Dict:
    """Compute pairwise Jaccard + symmetric differences for the modes."""
    modes = list(pools.keys())
    rep = {"jaccard": {}, "only_in": {}, "intersection": {}, "sizes": {}}
    for m in modes:
        rep["sizes"][m] = len(pools[m])
    for i, m1 in enumerate(modes):
        for m2 in modes[i + 1:]:
            j = jaccard(pools[m1], pools[m2])
            inter = pools[m1] & pools[m2]
            only1 = pools[m1] - pools[m2]
            only2 = pools[m2] - pools[m1]
            key = f"{m1}_vs_{m2}"
            rep["jaccard"][key] = round(j, 4)
            rep["intersection"][key] = sorted(inter)
            rep["only_in"][f"{m1}_not_{m2}"] = sorted(only1)
            rep["only_in"][f"{m2}_not_{m1}"] = sorted(only2)
    return rep


