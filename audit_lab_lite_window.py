"""Audit script — lab_lite TOTAL_CANDLES window scope (Caveat #21 §13.2).

Compares preset pools generated under three modes for a sample of symbols:

  Mode A (CURRENT, baseline 2026-04-19):
    Recent ~5000 bars (TOTAL_CANDLES=5000 hardcoded), regime-aware (GMM
    applied to recent window → 3 clusters → per-cluster preset selection).

  Mode B1 (FULL-HISTORY regime-agnostic):
    Full parquet history (~50-76K bars per sym). USE_CLUSTERING=False
    → single pool of presets ranked by global PnL/EMA score.

  Mode B2 (FULL-HISTORY per-cluster):
    Full parquet history + GMM applied to full series (cluster labels per
    bar) → per-cluster preset selection over full history. The MAs are
    computed on the CONTINUOUS series and only the scoring is masked by
    cluster.

Output:
  - audit_lab_lite_results_<TS>/preset_pools_{SYM}_{mode}.json
  - audit_lab_lite_results_<TS>/overlap_report.json (Jaccard A vs B1, A vs B2, B1 vs B2)
  - stdout summary

Designed for read-only audit (no productive code modified; uses backed-up
GMM models from regime_models/). Does NOT touch live/* or productive
pipeline.
"""
